"""
Al igual que con cualquier otro lenguaje de programación, los operadores de
suma (+),
resta (-),
multiplicación (*)
división (/) se pueden usar con números.
Además, Python tiene los operadores de potencia (**) y módulo (%).
"""
number = 9.0            # (float|int) number

result = number/2     # (float)

remainder = number%2  # (float|int)

print("result = " + str(result))
print("remainder = " + str(remainder))